def is_no_answer(text: str) -> bool:
    """Похоже ли, что ответ — честное «нет точного ответа» (а не содержательный ответ).
    Используется, чтобы по настройке не показывать источники под таким ответом."""
    t = (text or "").lower()
    return "нет точного ответа" in t


def build_context(hits: list[dict]) -> str:
    """hits: [{text, source, page, score}] -> текст для модели."""
    blocks = []
    for i, h in enumerate(hits, 1):
        loc = f"{h['source']}" + (f", стр. {h['page']}" if h.get("page") else "")
        blocks.append(f"[Фрагмент {i} — {loc}]\n{h['text']}")
    return "\n\n".join(blocks)


def _synonyms_block(question: str) -> str:
    """Подсказка по синонимам, встретившимся в вопросе (или '')."""
    try:
        import synonyms
        h = synonyms.hint(question)
        return (h + "\n\n") if h else ""
    except Exception:
        return ""


def build_user_message(question: str, context: str) -> str:
    return (
        f"КОНТЕКСТ:\n{context}\n\n"
        f"{_synonyms_block(question)}"
        f"ВОПРОС СОТРУДНИКА:\n{question}\n\n"
        f"Дай ответ по правилам выше."
    )
