#!/usr/bin/env bash
# =============================================================================
#  Включение расширенного режима (hybrid+) на GPU-сервере.
#  Ставит LightRAG в существующее окружение и строит граф знаний из документов.
#  Базовый вектор+реранк со структурными метаданными работает и без этого —
#  данный скрипт нужен только для слоя граф-RAG.
#
#  Запуск:  sudo bash gpu_variant/setup_hybrid.sh
# =============================================================================
set -euo pipefail
ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
RUN_USER="${SUDO_USER:-$(whoami)}"
cd "${ROOT_DIR}"

[[ -x ./.venv/bin/pip ]] || { echo "Нет .venv — сначала выполните первичную установку (run_gpu.sh)."; exit 1; }

echo "[hybrid] Ставлю LightRAG..."
sudo -u "${RUN_USER}" ./.venv/bin/pip install -q \
  lightrag-hku==1.3.0 nano-vectordb==0.0.4.3 tiktoken==0.8.0 networkx==3.4.2

echo "[hybrid] Строю граф знаний из DOCS_DIR (использует vLLM + bge-m3, это долго)..."
echo "          Совет: для пробы укажите в админке DOCS_DIR на подпапку из 30-50 файлов."
sudo -u "${RUN_USER}" ./.venv/bin/python -m graph_rag ingest

cat <<EOF

[hybrid] Готово. Теперь в админ-панели (раздел «Расширенный поиск (hybrid+)»):
  • LLM-метаданные  — включить и нажать «Переиндексировать» (добавит продукт/тему/тип);
  • Умные фильтры   — включить (фильтры из вопроса через LLM);
  • Граф-RAG        — включить (сводные вопросы пойдут в граф);
  • Режим графа     — mix по умолчанию.
EOF
