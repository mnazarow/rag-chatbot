def esc(s): return html.escape(str(s), quote=True)

def run(text, bold=False, italic=False, size=None):
    rpr = ""
    if bold: rpr += "<w:b/>"
    if italic: rpr += "<w:i/>"
    if size: rpr += f'<w:sz w:val="{size}"/><w:szCs w:val="{size}"/>'
    rpr = f"<w:rPr>{rpr}</w:rPr>" if rpr else ""
    return f'<w:r>{rpr}<w:t xml:space="preserve">{esc(text)}</w:t></w:r>'

def runs(x):
    return x if isinstance(x, str) and x.startswith("<w:r") else (
        "".join(x) if isinstance(x, list) else run(x))

def p(content="", style=None, align=None, after=120, before=0, line=276):
    ppr = ""
    if style: ppr += f'<w:pStyle w:val="{style}"/>'
    sp = []
    if before: sp.append(f'w:before="{before}"')
    sp.append(f'w:after="{after}"')
    if line: sp.append(f'w:line="{line}" w:lineRule="auto"')
    ppr += f'<w:spacing {" ".join(sp)}/>'
    if align: ppr += f'<w:jc w:val="{align}"/>'
    ppr = f"<w:pPr>{ppr}</w:pPr>"
    return f"<w:p>{ppr}{runs(content)}</w:p>"

def h1(t): return p(t, style="Heading1", after=160, before=280, line=0)
def h2(t): return p(t, style="Heading2", after=120, before=200, line=0)

def bullet(content):
    ppr = ('<w:pPr><w:pStyle w:val="LP"/><w:numPr><w:ilvl w:val="0"/>'
           '<w:numId w:val="1"/></w:numPr><w:spacing w:after="40" w:line="264" w:lineRule="auto"/>'
           '<w:jc w:val="both"/></w:pPr>')
    return f"<w:p>{ppr}{runs(content)}</w:p>"

def numitem(content):
    ppr = ('<w:pPr><w:pStyle w:val="LP"/><w:numPr><w:ilvl w:val="0"/>'
           '<w:numId w:val="2"/></w:numPr><w:spacing w:after="40" w:line="264" w:lineRule="auto"/>'
           '<w:jc w:val="both"/></w:pPr>')
    return f"<w:p>{ppr}{runs(content)}</w:p>"

def pagebreak(): return '<w:p><w:r><w:br w:type="page"/></w:r></w:p>'

def cell(w, content, fill=None, align=None, size=20, bold=False):
    shd = f'<w:shd w:val="clear" w:color="auto" w:fill="{fill}"/>' if fill else ""
    tcpr = (f'<w:tcPr><w:tcW w:w="{w}" w:type="dxa"/>{shd}'
            f'<w:tcMar><w:top w:w="40" w:type="dxa"/><w:left w:w="110" w:type="dxa"/>'
            f'<w:bottom w:w="40" w:type="dxa"/><w:right w:w="110" w:type="dxa"/></w:tcMar>'
            f'<w:vAlign w:val="top"/></w:tcPr>')
    if isinstance(content, str) and content.startswith("<w:p"):
        body = content
    else:
        jc = f'<w:jc w:val="{align}"/>' if align else ""
        body = (f'<w:p><w:pPr><w:spacing w:after="0" w:line="252" w:lineRule="auto"/>{jc}</w:pPr>'
                f'{run(content, bold=bold, size=size)}</w:p>')
    return f"<w:tc>{tcpr}{body}</w:tc>"

def table(grid, rows):
    borders = ('<w:tblBorders>'
               + "".join(f'<w:{s} w:val="single" w:sz="4" w:space="0" w:color="888888"/>'
                         for s in ("top","left","bottom","right","insideH","insideV"))
               + '</w:tblBorders>')
    tblpr = (f'<w:tblPr><w:tblW w:w="{sum(grid)}" w:type="dxa"/>{borders}'
             f'<w:tblLayout w:type="fixed"/></w:tblPr>')
    gridxml = '<w:tblGrid>' + "".join(f'<w:gridCol w:w="{g}"/>' for g in grid) + '</w:tblGrid>'
    rowsxml = ""
    for cells, header in rows:
        trpr = '<w:trPr><w:tblHeader/></w:trPr>' if header else ""
        rowsxml += f"<w:tr>{trpr}{''.join(cells)}</w:tr>"
    return f"<w:tbl>{tblpr}{gridxml}{rowsxml}</w:tbl>"

